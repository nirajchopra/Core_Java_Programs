package com.javabasics;


public class NullLiteral {

	String str;
	NullLiteral nl;
	System s1;

	public static void main(String[] args) {
		NullLiteral obj = new NullLiteral();
		System.out.println(" str : " + obj.str);
		System.out.println(" nl : " + obj.nl);
		System.out.println(" s1 : " + obj.s1);

	}

}
            
