package com.rays.JavaBasics;

public class Array {
	public static void main(String[] args) {
		int marks[] = new int[4];
		marks[0] = 90; // Maths
		marks[1] = 91; // Chemistry
		marks[2] = 92; // Physics
		marks[3] = 93; // English

		for (int i = 0; i < 4; i++) {
			System.out.println(marks[i]);
		}

	}

}
