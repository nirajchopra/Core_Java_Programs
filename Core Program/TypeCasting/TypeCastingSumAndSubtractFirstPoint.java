package TypeCasting;
import java.util.*;

public class TypeCastingSumAndSubtractFirstPoint {
    public static void main(String[] args) {
        
        char a = 'a';
        char b = 'b';

        System.out.println((int)(b));
        System.out.println((int)(a));

        System.out.println("Sum of TypeCasting : " + (a+b));
        System.out.println("Product of TypeCasting : " + (b-a));

    }
}
