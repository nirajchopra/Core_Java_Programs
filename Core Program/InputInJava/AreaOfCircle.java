package InputInJava;
import java.util.*;

public class AreaOfCircle {
     public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // float PI = 3.14f;
        float rad = sc.nextFloat();
        // float area = PI * rad * rad;
        float area = 3.14f * rad * rad;


        System.out.println("Area Of circle = " + area);
     }
}
